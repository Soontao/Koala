package org.fornever.koala.enums;

public enum ConditionOperator {
	/**
	 * full equal value
	 */
	EQUAL,
	/**
	 * not equal value
	 */
	NOT_EUQAL,
	/**
	 * >
	 */
	GREATER_THAN,
	/**
	 * >=
	 */
	GREATER_EQUAL,
	/**
	 * <
	 */
	LESS_THAN,
	/**
	 * <=
	 */
	LESS_EQUAL,
	/**
	 * fuzzy match
	 */
	LIKE,
	/**
	 * equal in list
	 */
	IN
}
